#include "libft.h"

void	free_s_arr(char ***s_arr)
{
	int	i;

	if (!(*s_arr))
		return ;
	i = 0;
	while ((*s_arr)[i])
		free((*s_arr)[i++]);
	free(*s_arr);
	*s_arr = NULL;
}
