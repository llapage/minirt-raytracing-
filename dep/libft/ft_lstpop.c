#include "libft.h"

t_list	*ft_lstpop(t_list **lst)
{
	t_list	*lst_ptr;

	if (!(lst))
		return (NULL);
	lst_ptr = *lst;
	*lst = (*lst)->next;
	return (lst_ptr);
}
